def tariff_desc_key_from_payment_callback(tariff: str) -> str:
    """Ключ цены из callback кнопки оплаты (r_m3_d3 / gift_r_m3_d3 → m3_d3)."""
    if tariff.startswith('gift_r_'):
        return tariff[len('gift_r_'):]
    if tariff.startswith('r_'):
        return tariff[2:]
    return tariff


